package com.silicon.model;

public class FacultyDetails {
	private int facultyid;
	private String facultyname;
	private String password;
	private String facmob;
	private String facmail;
	private String address;
	public int getFacultyid() {
		return facultyid;
	}
	public void setFacultyid(int facultyid) {
		this.facultyid = facultyid;
	}
	public String getFacultyname() {
		return facultyname;
	}
	public void setFacultyname(String facultyname) {
		this.facultyname = facultyname;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public String getFacmob() {
		return facmob;
	}
	public void setFacmob(String facmob) {
		this.facmob = facmob;
	}
	public String getFacmail() {
		return facmail;
	}
	public void setFacmail(String facmail) {
		this.facmail = facmail;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
}
